package de.romanamo.explorino.math;

/**
 * Class to represent a point in 2D-Space.
 */
public class Point {

    public int x;

    public int y;

    /**
     * Constructs a point with coordinates.
     *
     * @param x x-coordinate
     * @param y y-coordinate
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
